    public Oper_view(int cid, int pid, String nam) {

        this.cid = cid;
        this.pid = pid;

        initComponents();
        this.setTitle("Operation View Form");
        nam1 = nam;

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/crm_project", "root", "root");
            PreparedStatement pst = (PreparedStatement) con.prepareStatement("select * from add_client where client_id=?");
            pst.setInt(1, cid);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                name.setText(rs.getString("name"));
                add.setText(rs.getString("address"));
                email.setText(rs.getString("email"));
                co1.setText(rs.getString("contact"));
                cmm.setText(Integer.toString(rs.getInt("cmm_level")));
                aco1.setText(rs.getString("altno"));
                cn.setText((rs.getString("cmp_name")));
                bch.setText(rs.getString("branch"));
                domain.setText(rs.getString("domain_services"));

            }
            PreparedStatement pst1 = (PreparedStatement) con.prepareStatement("select start_date,end_date,technology, proj_desc from add_proj where client_id=? AND proj_id=?");
            pst1.setInt(1, cid);
            pst1.setInt(2, pid);

            ResultSet rs1 = pst1.executeQuery();
            while (rs1.next()) {
                sd1.setText(rs1.getString("start_date"));
                ed1.setText(rs1.getString("end_date"));
                tec.setText(rs1.getString("technology"));
                desc.setText(rs1.getString("proj_desc"));
            }

            PreparedStatement pst2 = (PreparedStatement) con.prepareStatement("select * from oper_dept where client_id=? ");
            pst2.setInt(1, cid);
            ResultSet rs2 = pst2.executeQuery();

            while (rs2.next()) {
                sop.setText(rs2.getString("status_project"));
                history.setText(rs2.getString("history"));
                feed.setText(rs2.getString("feedback"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }




    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        System.out.println("pid: " + pid);
        System.out.println("cid: " + cid);

        String a = sop.getText();
        String b = history.getText();
        String c = feed.getText();
        String d = sd1.getText();
        String e = ed1.getText();
        String f = tec.getText();
        String g = desc.getText();

        try {

            Class.forName("com.mysql.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/crm_project", "root", "root");
            PreparedStatement pst = (PreparedStatement) con.prepareStatement("update oper_dept set status_project = ?, history = ?, feedback = ? where client_id=? AND proj_id=?");

            pst.setString(1, a);
            pst.setString(2, b);
            pst.setString(3, c);
            pst.setInt(4, cid);
            pst.setInt(5, pid);
            if (pst.executeUpdate() == 0) {
                pst = (PreparedStatement) con.prepareStatement("insert into oper_dept values(?, ?, ?, ?, ?)");

                pst.setInt(1, cid);
                pst.setString(2, a);
                pst.setString(3, b);
                pst.setString(4, c);
                pst.setInt(5, pid);
                pst.executeUpdate();
            }

            pst = (PreparedStatement) con.prepareStatement("update add_proj set start_date = ?, end_date = ?, technology = ?, proj_desc = ? where proj_id = ?");

            pst.setString(1, d);
            pst.setString(2, e);
            pst.setString(3, f);
            pst.setString(4, g);
            pst.setInt(5, pid);
            System.out.println("Status II: " + pst.executeUpdate());

            JOptionPane.showMessageDialog(null, "Saved!");

            this.dispose();
            new OA_select("Operation", nam1).setVisible(true);

        } catch (Exception ex) {
            ex.printStackTrace();
        }


    }//GEN-LAST:event_jButton1ActionPerformed


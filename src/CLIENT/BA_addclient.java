    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String fnam = fname.getText();
        String addrs = adrs.getText();
        String eml = mail.getText();
        String cont = connt.getText();
        String altnu = altno.getText();
        int cml = Integer.parseInt((String) cmmlev.getSelectedItem());
        String cmpname = comname.getText();
        String brnch = bnch.getText();
        String dsw = dswork.getText();

        try {

            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/crm_project", "root", "root");

            PreparedStatement pr;
            
            if(imHereToEdit) {
                pr = (PreparedStatement) conn.prepareStatement("update add_client set name = ?, address = ?, email = ?, contact = ?, altno = ?, cmm_level = ?, cmp_name = ?, branch = ?, domain_services = ? where client_id = ?");
            } else {
                pr = (PreparedStatement) conn.prepareStatement("insert into add_client "
                    + "(name,address,email,contact,altno,cmm_level,cmp_name,branch,domain_services) values (?,?,?,?,?,?,?,?,?)");
            }
            
            if (fnam.equals("") || addrs.equals("") || eml.equals("") || brnch.equals("") || dsw.equals("")) {
                JOptionPane.showMessageDialog(null, "invalid ");

            } else {
                pr.setString(1, fnam);
                pr.setString(2, addrs);
                pr.setString(3, eml);
                pr.setString(4, cont);
                pr.setString(5, altnu);
                pr.setInt(6, cml);
                pr.setString(7, cmpname);
                pr.setString(8, brnch);
                pr.setString(9, dsw);
                if(imHereToEdit)
                    pr.setInt(10, id);
                pr.executeUpdate();
                JOptionPane.showMessageDialog(null, "Saved!");

            }
            this.dispose();
            new avd().setVisible(true);
        } catch (Exception e) {
            e.getMessage();
        }

    }//GEN-LAST:event_jButton2ActionPerformed


    public BA_addclient(int id) {
        initComponents();
        this.setTitle("Update Client");
        imHereToEdit = true;
        this.id = id;
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/crm_project", "root", "root");
            PreparedStatement pr = (PreparedStatement) conn.prepareStatement("select * from add_client where client_id = '" + id + "'");
            ResultSet rs = pr.executeQuery();
            if (rs.next()) {
                fname.setText(rs.getString("name"));
                adrs.setText(rs.getString("address"));
                mail.setText(rs.getString("email"));
                connt.setText(rs.getString("contact"));
                altno.setText(rs.getString("altno"));
                cmmlev.setSelectedItem(rs.getString("cmm_level"));
                comname.setText(rs.getString("cmp_name"));
                bnch.setText(rs.getString("branch"));
                dswork.setText(rs.getString("domain_services"));
            }
        } catch (Exception e) {
        }
    }

    boolean validateEmail(String email) {  //To Validate Email ID 
        //Set the email pattern string
        Pattern p = Pattern.compile(".+@.+\\.[a-z]+");
        //Match the given string with the pattern
        Matcher m = p.matcher(email);
        //check whether match is found

        boolean matchFound = m.matches();
        if (matchFound) {
            JOptionPane.showMessageDialog(this, "Valid Email Id.");
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Email Id.");
            return false;
        }
    }


    public HR_edit(boolean imHereToEdit, String dept, String nam, int id) {
        name1 = nam;
        this.id = id;
        this.imHereToEdit = imHereToEdit;
        initComponents();
        if (imHereToEdit) {
            jButton2.setText("Save");
        } else {
            jButton2.setText("Add");
        }

        this.setTitle("Client Detail");

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/crm_project", "root", "root");
            java.sql.PreparedStatement pst = (java.sql.PreparedStatement) con.prepareStatement("select * from add_client where client_id=?");
            pst.setInt(1, id);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                cname.setText(rs.getString("name"));
                addr.setText(rs.getString("address"));
                mail.setText(rs.getString("email"));
                cont.setText(rs.getString("contact"));
                acon1.setText(rs.getString("altno"));
                cmmlev.setText(rs.getString("cmm_level"));
                cmna.setText(rs.getString("cmp_name"));
                brnch.setText(rs.getString("branch"));
                jLabel10.setText(rs.getString("domain_services"));

                if (imHereToEdit) {
                    pst = (java.sql.PreparedStatement) con.prepareStatement("select * from hr_dept where client_id=?");
                    pst.setInt(1, id);
                    rs = pst.executeQuery();
                    if (rs.next()) {
                        bg.setText(rs.getString("cli_background"));
                        his.setText(rs.getString("history"));
                        feed.setText(rs.getString("feedback"));
                        comm.setText(rs.getString("comments"));
                    }
                }

            } else {
                JOptionPane.showMessageDialog(null, "No record found!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String b = bg.getText();
        String c = his.getText();
        String d = feed.getText();
        String e = comm.getText();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/crm_project", "root", "root");
            PreparedStatement pst;
            if (imHereToEdit) {
                pst = (PreparedStatement) con.prepareStatement("update hr_dept set cli_background =?, history =?, feedback =?, comments =? where client_id = ?");
                pst.setString(1, b);
                pst.setString(2, c);
                pst.setString(3, d);
                pst.setString(4, e);
                pst.setInt(5, id);
                System.out.println("Updaing...");
            } else {
                pst = (PreparedStatement) con.prepareStatement("insert into hr_dept(client_id,cli_background,history,feedback,comments)values(?,?,?,?,?)");
                pst.setInt(1, id);
                pst.setString(2, b);
                pst.setString(3, c);
                pst.setString(4, d);
                pst.setString(5, e);
                System.out.println("Inserting...");
            }


            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Added");
            this.dispose();
            new selectclient("", name1).setVisible(true);

        } catch (Exception q) {
            q.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

